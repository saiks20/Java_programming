package com.javaj2ee.datatypes;

public class NumberSystem {
    public static void main(String[] args){
        int dec1=132;
        int binNum=0b1010110;
        int hexNum=0x10ABC;
        int octNum=01744;
        System.out.println("the decimal number is: "+dec1);
        System.out.println("the binary number is: "+binNum);
        System.out.println("the hexa number is: "+hexNum);
        System.out.println("the octal number is: "+octNum);


    }
}
